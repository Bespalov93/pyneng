"""
Задание 15.1

Создать функцию get_ip_from_cfg, которая ожидает как аргумент имя файла,
в котором находится конфигурация устройства.

Функция должна обрабатывать конфигурацию и возвращать IP-адреса и маски,
которые настроены на интерфейсах, в виде списка кортежей:
* первый элемент кортежа - IP-адрес
* второй элемент кортежа - маска

Например (взяты произвольные адреса):
[('10.0.1.1', '255.255.255.0'), ('10.0.2.1', '255.255.255.0')]

Для получения такого результата, используйте регулярные выражения.

Проверить работу функции на примере файла config_r1.txt.


Обратите внимание, что в данном случае, можно не проверять корректность IP-адреса,
диапазоны адресов и так далее, так как обрабатывается вывод команды,
а не ввод пользователя.

"""
import re

def get_ip_from_cfg(filename):
    # Список для хранения найденных IP-адресов и масок
    ip_list = []

    # Регулярное выражение для поиска строки с IP-адресом и маской
    ip_pattern = re.compile(r"ip address (\d+\.\d+\.\d+\.\d+) (\d+\.\d+\.\d+\.\d+)")

    # Открываем файл и обрабатываем его построчно
    with open(filename, 'r') as f:
        for line in f:
            # Ищем совпадения с регулярным выражением
            match = ip_pattern.search(line)
            if match:
                # Если совпадение найдено, добавляем IP и маску в список
                ip_address = match.group(1)
                subnet_mask = match.group(2)
                ip_list.append((ip_address, subnet_mask))
    
    return ip_list
